            <div class="col-lg-7">
                <p class="text-justify mb-4">
                    School buildings are precious – not because of their expensive
                    infrastructure but because of the tender souls which frolic inside.
                    With great hope and faith, the parents entrust their child to the
                    school. It is therefore, school’s first duty to provide a safe
                    environment to one and all. Keeping all internal and external
                    threats and hazards in mind, the school observes the following
                    safety measures -
                </p>
                <ul class="text-justify ps-3">
                    <li>
                        Students of Nursery, Prep I and Prep II are escorted to their classrooms from
                        their buses by their teachers and helpers.
                    </li>
                    <li>
                        The entire school and school buses are equipped with HD CCTV
                        cameras.
                    </li>
                    <li>
                        All buses have a GPS tracking system & a female staff member.
                    </li>
                    <li>The school has a state-of-the-art fire-fighting system.</li>
                    <li>
                        Central Security Alarm is installed & the entire staff is trained
                        to deal with emergencies.
                    </li>
                    <li>
                        Metal detectors are installed at the gates. The School Compound
                        has an electric fence.
                    </li>
                    <li>
                        Each block houses separate boys and girls washrooms manned by
                        attendants 24 X 7.
                    </li>
                    <li>
                        Visitors / Parents are allowed to enter the school premises after
                        a complete security check and verification at the gate.
                    </li>
                    <li>
                        It is mandatory for everyone to wear their ID card inside the
                        school.
                    </li>
                    <li>The entire DPS Agra team is verified by the police.</li>
                    <li>
                        Regular Security Awareness workshops are conducted for staff &
                        students.
                    </li>
                </ul>
