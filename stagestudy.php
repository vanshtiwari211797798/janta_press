            <p class="text-justify">
              Early Childhood Care and Education ideally consists of flexible, multi-faceted, multi-level, play-based, activity-based, and inquiry-based learning, comprising of alphabets, languages, numbers, counting, colours, shapes, indoor and outdoor play, puzzles and logical thinking, problem-solving, drawing, painting and other visual art, craft, drama and puppetry, music and movement. It also includes a focus on developing social capacities, sensitivity, good behaviour, courtesy, ethics, personal and public cleanliness, teamwork, and cooperation.
              There is no contour of knowledge and each student gets an opportunity to participate in group activities and project work. More emphasis is paid on learning by doing along with cognitive approach. 
              We emphasise on -
              <br>•	Development based curriculum for learning essential concepts of all the subjects.
              <br>•	Conducive teaching-learning environment.
              <br>•	Developing positive attitude, healthy habits and values like dignity, honesty and morality.
              <br>•	Innovative use of technology that helps to garner and harness the varying potential within each student for which we have smart classrooms equipped with 65” LED screens and Glass Boards.
              <br>•	Providing 21st century information using modern resources and technology. Combining the traditional library collections and services with up-to-date computer labs and internet-accessible resources. 
              <br>•	Co-scholastic activities that encompass everything from traditional sports such as swimming, skating, lawn tennis, yoga, football, hockey, cricket and athletics to more culturally oriented pursuits like art, dance, drama and music, along with a host of other opportunities through numerous events with 100% participation.
              <br>•	Having separate play fields for different sports. We have the most modern Jungle Gym and a field equipped with Chess Board.
              <br>•	Regular assessments to understand and record every student’s learning curve and capabilities; and to provide a way forward for progress.
