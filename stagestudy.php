          <div class="card-body">
            <p class="text-justify mb-0">
              The Secondary Stage will comprise of four years of multidisciplinary study, building on the subject-oriented pedagogical and curricular style of the Middle Stage, but with greater depth, greater critical thinking, greater attention to life aspirations, and greater flexibility and student choice of subjects. 
The school has personalized study strategy to help students plan better and study smarter. Different diagnostic assessments are made by designing different worksheets to bridge the gap in learning and prepare the students to face the All-India Secondary School Curriculum.
This pattern of education is leading to the All-India Secondary School Certificate Examination (AISSCE) conducted by the Central Board of Secondary Education, New Delhi. The school divides its education into three main streams i.e., Science, Commerce and Humanities according to the student’s aptitude and interest. We have four main goals for our students-
<br>1. Performance in Board Exams.
<br>2. Praiseworthy character
<br>3. Pleasing personality
<br>4. Preparing for future
