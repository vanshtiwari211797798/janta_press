    <h2 class="text-center mb-4">Stages of Study</h2>
    <p class="text-justify">
      The National Education Policy envisions an education system rooted in Indian ethos that contributes directly to transforming India. 
      The Policy envisages that the curriculum and pedagogy of our institutions must develop among the students a deep sense of respect 
      towards the Fundamental Duties and Constitutional values, bonding with one’s country, and a conscious awareness of one’s roles and 
      responsibilities in a changing world. The vision of the Policy is to instill among the learners a deep-rooted pride in being Indian, 
      not only in thought, but also in spirit, intellect, and deeds, as well as to develop knowledge, skills, values, and dispositions that 
      support responsible commitment to human rights, sustainable development and living, and global well-being, thereby reflecting a 
      truly global citizen.
    </p>
    <h5 class="text-left mb-4">Restructuring school curriculum and pedagogy in a new 5+3+3+4 design</h5>
    <p class="text-justify">
      The curricular and pedagogical structure of school education will be reconfigured to make it responsive and relevant to the 
      developmental needs and interests of learners at different stages of their development, corresponding to the age ranges of 3-8,
      8-11, 11-14, and 14-18 years, respectively.  The curricular and pedagogical structure and the curricular framework for school 
      education will therefore be guided by a 5+3+3+4 design, consisting of the Foundational Stage (in two parts, that is, 3 years of
      pre-school + 2 years in primary school in Grades 1-2; both together covering ages 3-8), Preparatory Stage (Grades 3-5, covering ages 8-11), Middle Stage (Grades 6-8, covering ages 11-14), and Secondary Stage (Grades 9-12 in two phases, i.e., 9 and 10 in the first   and 11 and 12 in the second, covering ages 14-18).
      Our pedagogy is not confined to the books only, it is based on Project based Learning, Experiential Learning and Concerted learning with
      the aim to blend consistency and variety in teaching methods to foster the intellectual growth required to achieve academic success. 
      All this is adopted with the aim to help students improve their learning outcomes and achieve their full potential. 

    </p>
