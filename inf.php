        Hall, Volleyball, Basketball Court, International Standard Synthetic
        Tennis Courts, Squash Court, 25 m Swimming Pool and a small pool for
        beginners, Dance Room, Indian and Western Music Rooms, Audio-Video
        Recording Room, In-house Infirmary with a team of qualified doctors on
        duty, Counselor Room, Smart Classes with Multimedia Facilities in all
        classrooms, Computer Labs, Two well stocked Libraries, Three Large
        Science Labs, Biotech Lab., Art Studio, Multipurpose Hall,
        Air-conditioned Auditorium, Math Lab., Humanities Lab., Language Lab.,
        Open-Air-Plaza etc. which are integral parts of our infrastructure. The
        curriculum is designed to ensure all round development of each student.
        Besides a comprehensive curriculum, a technologically designed
        methodology is introduced at all stages to develop skills required to
        achieve academic success. The medium of instruction is English and
        special emphasis is laid both on written and spoken English at all
        levels. It is obligatory for all students to speak in English within the
        school premises. The scheme of studies is structured and flexible and
        the link between Pre-Primary, Primary, Middle, Secondary and Senior
        Secondary stages is well integrated and phased out.
